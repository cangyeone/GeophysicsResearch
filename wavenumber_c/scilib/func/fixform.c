int CpxM2inv(cplx A[2][2],cplx B[2][2])
{
	register cplx mod;
	mod=-A[0][1]*A[1][0]+A[0][0]*A[1][1];

	B[0][0]=A[1][1]/mod;
	B[0][1]=-A[0][1]/mod;
	B[1][0]=-A[1][0]/mod;
	B[1][1]=A[0][0]/mod;
	if(mod==0)return 0;
	return 1;

}
